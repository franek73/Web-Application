		Historia Adolphe'a Saxa
	</h2>
	<h3>Antoine Joseph Sax, zwany Adolphe (1814-1894) – belgijski budowniczy instrumentów muzycznych, konstruktor 
		saksofonu.
	</h3>
	<p>
		6 listopada 1814 roku urodziło się pierwsze z jedenaściorga dzieci Charlesa Josepha Saxa – Antoine Joseph Sax zwany później Adolfem. Charles Sax, fabrykant, 
		właściciel fabryki instrumentów dętych, w rok po bitwie pod Waterloo mianowany został przez króla Zjednoczonych Niederlandów, Wilhelma I głównym dostawcą 
		instrumentów odradzającej się armii.
	</p>
	<p>
		W mieście Dinant było dużo drobnych warsztatów mechanicznych, wśród których europejską sławę zdobyły warsztaty blacharskie, a szczególnie te, które pracowały 
		na blachach z żółtej miedzi i mosiądzu. Adolf Sax od wczesnych lat zaznajamiał się z kształtem, budową i technologią produkcji instrumentów dętych. W wieku 
		sześciu lat potrafił wytoczyć korpus klarnetu i roztrąb rogu. Od tego czasu pobierał systematyczne lekcje gry na flecie w Konserwatorium Brukselskim i klarnecie 
		u znanego dyrygenta orkiestr wojskowych – L. Bendera.
	</p>
	<p>
		W roku 1829, mając piętnaście lat, posłał na wystawę do Paryża wykonane własnoręcznie dwa flety i klarnet z kości słoniowej, które to instrumenty 
		zostały uznane za świetne.
	</p>
	<p>
		W wieku 20. lat udoskonalił klarnet basowy, będący do tej pory instrumentem bardzo niestrojnym i nierównym brzmieniowo. Od tego czasu zaczął borykać się ze 
		złymi opiniami, przeciwnościami, a nawet procesami ze strony konkurencji i muzyków, którzy powodowani zazdrością byli Saksowi niechętni. Pierwsze spory 
		dotyczyły unowocześnionego klarnetu basowego i dopiero konfrontacja instrumentów starych i nowego spowodowała, że wynalazek został zaakceptowany.
	</p>
	<p>
		Wiosną 1841 roku Adolf Sax przeniósł się na jakiś czas do Paryża, gdzie obok uznania jako konstruktora i wynalazcy spotkała go zawiść, napady krytyków i 
		muzyków, oszczerstwa, procesy sądowe, a nawet kradzieże. Z drugiej strony Sax miał wyjątkową zdolność robienia sobie nieprzyjaciół wynikającą z arogancji i 
		trudnego usposobienia.
	</p>
