	<h2 class="typy">
		Historia saksofonu
	</h2>
	<p>
		Historia saksofonu rozpoczęła się najprawdopodobniej w roku 1841, kiedy to przygotowany na wystawę instrumentów w Brukseli pakunek
		 ktoś kopnął w niewyjaśnionych okolicznościach. Pogięty i zniszczony instrument nie nadawał się  do prezentacji. Sądząc z opisów, pakunek 
		 zawierał nowy, skonstruowany niedawno instrument – saksofon.</p>
<p>Pierwsza wzmianka o saksofonie, autorstwa Hektora Berlioza, ukazała się 12 czerwca 1842 roku w paryskim Journal des Debats i ta 
	data została przyjęta za metrykę powstania saksofonu, chociaż instrument miał swoją publiczną premierę 3 lutego 1844 r., a opatentowany 
	został dopiero 28 czerwca roku 1846. Saksofon (Le Saxophon), nazywany po jego wynalazcy, był instrumentem z grupy dętych drewnianych. Miał dziewiętnaście 
	klap, co uczyniło go podobnym do instrumentu ophicleide.</p>

<p>Ustnik, w przeciwieństwie do ustników instrumentów dętych blaszanych, był podobny do ustnika klarnetu basowego. Co ciekawe, pierwszym opisywanym saksofonem był saksofon 
	basowy w stroju C; dopiero później, z konieczności nadania większej ruchliwości, Adolf Sax skonstruował baryton, alt, tenor i sopran – mniejsze odmiany saksofonu.
Skala pierwszego saksofonu była o siedem dźwięków większa niż późniejsze modele, które Sax przebudował ze względu na gorsze możliwości
 brzmieniowe najwyższych dźwięków prototypu.
</p>

<p>Wiosną 1843 roku rozpoczął produkcję instrumentów blaszanych z nowym mechanizmem obrotowym, zamiast tłokowym. W listopadzie 1843 roku, 
	podczas wystawienia opery Don Sebastian Donizettiego, muzycy odmówili wykonania partii klarnetu basowego 
	na instrumencie zrobionym przez Saxa. Sax zadeklarował, że wykona ją osobiście, ale muzycy zagrozili strajkiem. Sax wspólnie z Donizettim musieli ustąpić.
</p>

<p>Wkrótce po premierze pierwszego saksofonu Adolf Sax zademonstrował swoje instrumenty w Konserwatorium Paryskim,
	 gdzie słuchali go znani wówczas muzycy i kompozytorzy: Auber (sławny wówczas  kompozytor La Muette de Portici – opery wykonywanej w 
	 Brukseli 21 września 1830, będącej sygnałem dla Rewolucji Belgijskiej), Halévy, Habeneck, Monnais i inni, którzy chwalili Saxa za nowatorstwo
	  i muzykalność. Po sukcesie prezentacji Sax otworzył warsztat w Paryżu przy ulicy Saint Georges 10, gdzie zabrał się energicznie do
	 pracy, nader często jednak popadając w konflikty z zatrudnianymi rzemieślnikami. Na siedemnastego i dwudziestego pierwszego czerwca 
	 przypadają daty pierwszych francuskich patentów saksofonowych.</p>

 <p>Na początku 1844 roku warsztat Saxa odwiedził Rossi, który urzeczony brzmieniem saksofonu sprowadził go do
	 Konserwatorium w Bolonii. Najwierniejszym zwolennikiem saksofonu i talentu Saxa był jednak Hektor Berlioz – gorący propagator 
	 nowego instrumentu, konsekwentnie wszędzie tam, gdzie koncertował.</p>
<p>Latem 1844 saksofon został pokazany na Paris Industrial Exhibision i prezentowany wobec Króla Louisa Phollippe i Królowej Marie Amelie.</p>

<p>Od roku 1844 warsztat Saxa odwiedzała śmietanka życia muzycznego, literackiego i społecznego Paryża. Przy każdej okazji Sax demonstrował 
	możliwości swoich instrumentów, podczas improwizowanych koncertów. W tym samym roku Sax otworzył filię fabryki w Anglii, a wkrótce po 
	tym jego młodszy brat Henry wyemigrował do Ameryki, gdzie założył manufakturę instrumentów dętych w Filadelfii. Latem 1844 saksofon został 
	pokazany na Paris Industrial Exhibision i prezentowany wobec Króla Louisa Phollippe i Królowej Marie Amelie.</p>

<p>Rok 1845 okazał się przełomowy w życiu Adolfa Saxa. Dopiero teraz zaczęła przełamywać się stopniowa niechęć muzyków i ś
	rodowiska konstruktorów instrumentów. Chociaż nadal miał wielu wrogów, stopniowo pokonywał zniechęcenie do swojej osoby. Budził 
	olbrzymie kontrowersje, nazywając grupę saxhornów swoim nazwiskiem (protestowali niemieccy konstruktorzy: Stolzel i Blumel). W
	 tym samym roku Sax otworzył filię fabryki w Anglii, a wkrótce po tym jego młodszy brat Henry wyemigrował do Ameryki, gdzie w Filadelfii 
	 założył manufakturę instrumentów dętych.</p>

<p>W 1858 roku Sax został  kierownikiem klasy saksofonu przy Konserwatorium Paryskim, w którym nauczał przez trzynaście lat.</p>

<p>Po okresie prosperity, kilkanaście lat po premierze saksofonu, kariera Saxa podupadła. Doszło do tego, że Cesarz Napoleon III ratował go od bankructwa.</p>

<p>W roku 1870 rząd rozwiązał klasę. Do końca życia żył w biedzie, na emeryturze ustanowionej przez rząd. Zmarł w siódmego lutego 1894 roku.
	</p>
